                <section class="my-5">
                    <h3 class="text-lg font-bold mb-2">Estrutura na forma afirmativa com auxiliar</h3>
                    <p>[sujeito] + [auxiliar (se houver)] + [verbo principal] + [resto da frase]</p>
                    <p>Exemplos:</p>
                    <ul class="list-disc pl-6">
                        <li><strong>I am studying English.</strong> -> Eu estou estudando inglês.</li>
                        <li><strong>She is working now.</strong> -> Ela está trabalhando agora.</li>
                        <li><strong>I have finished my homework.</strong> -> Eu terminei meu dever de casa.</li>
                        <li><strong>They have lived here for ten years.</strong> -> Eles moram aqui há dez anos.</li>
                        <li><strong>I do like this song!</strong> -> Eu realmente gosto dessa música!</li>
                        <li><strong>He does want to go with us.</strong> -> Ele realmente quer ir com a gente.</li>
                        <li><strong>You can speak Spanish well.</strong> -> Você sabe falar espanhol bem.</li>
                        <li><strong>We will travel next year.</strong> -> Nós vamos viajar no ano que vem.</li>
                    </ul>
                </section>
            </div>
        </article>
