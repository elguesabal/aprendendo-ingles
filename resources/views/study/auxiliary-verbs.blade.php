                <section class="my-5">
                    <h3 class="text-lg font-bold mb-2">Estrutura da pergunta com auxiliar</h3>
                    <p>[auxiliar] + [sujeito] + [verbo principal] + [resto da frase]?</p>
                    <p>Exemplos:</p>
                    <ul class="list-disc pl-6">
                        <li><strong>Do you like music?</strong> -> Você gosta de música?</li>
                        <li><strong>Did he call you?</strong> -> Ele ligou para você?</li>
                        <li><strong>Are you happy?</strong> -> Você está feliz?</li>
                        <li><strong>Were they tired?</strong> -> Eles estavam cansados?</li>
                        <li><strong>Have you eaten?</strong> -> Você comeu?</li>
                        <li><strong>Will she travel?</strong> -> Ela vai viajar?</li>
                    </ul>
                </section>
