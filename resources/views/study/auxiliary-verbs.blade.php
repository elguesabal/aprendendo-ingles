                <section class="my-5">
                    <h2 class="text-3xl font-bold mb-2">O que são verbos auxiliares?</h2>
                    <p>Verbos auxiliares são verbos que ajudam a formar frases interrogativas (perguntas), negativas ou tempos verbais.</p>
                    <p>Eles não trazem o sentido principal, só ajudam a estruturar a frase corretamente.</p>
                </section>
