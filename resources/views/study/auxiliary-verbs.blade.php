                <section class="my-5">
                    <h3 class="text-lg font-bold mb-2">Estrutura da forma negativa com auxiliar</h3>
                    <p>[sujeito] + [auxiliar] + not + [verbo principal] + [resto da frase]</p>
                    <p>O “not” é o que transforma a frase em negativa.</p>
                    <p>Exemplos:</p>
                    <ul class="list-disc pl-6">
                        <li><strong>I am not studying English.</strong> -> Eu não estou estudando inglês</li>
                        <li><strong>She is not working now.</strong> -> Ela não está trabalhando agora.</li>
                        <li><strong>I have not finished my homework.</strong> -> Eu não terminei meu dever de casa.</li>
                        <li><strong>They have not lived here for ten years.</strong> -> Eles não moram aqui há dez anos.</li>
                        <li><strong>I do not like this song! </strong> -> Eu realmente não gosto dessa música!</li>
                        <li><strong>You can not speak Spanish well.</strong> -> Você não sabe falar espanhol bem.</li>
                        <li><strong>We will not travel next year.</strong> -> Nós não vamos viajar no ano que vem.</li>
                    </ul>
                </section>
            </div>
        </article>
